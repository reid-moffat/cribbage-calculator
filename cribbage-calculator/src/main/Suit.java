package main;

/**
 * The four card suits in a standard deck of 52 playing cards, ordered from
 * lowest to highest value using alphabetical order:
 * 
 * <ul>
 * 	<li>{@code CLUBS}
 * 	<li>{@code DIAMONDS}
 * 	<li>{@code HEARTS}
 * 	<li>{@code SPADES}
 * <ul>
 * 
 */
enum Suit {
	/**
	 * The suit clubs
	 */
	CLUBS,

	/**
	 * The suit diamonds
	 */
	DIAMONDS,

	/**
	 * The suit hearts
	 */
	HEARTS,

	/**
	 * The suit spades
	 */
	SPADES
}
