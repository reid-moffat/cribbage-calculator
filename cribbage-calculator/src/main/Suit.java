package main;

/**
 * The four card suits in a standard deck of 52 playing cards, ordered from
 * lowest to highest value using alphabetical order:
 * 
 * <ul>
 * 	<li></code>CLUBS<code>
 * 	<li></code>DIAMONDS<code>
 * 	<li></code>HEARTS<code>
 * 	<li></code>SPADES<code>
 * <ul>
 * 
 */
enum Suit {
	/**
	 * The suit clubs
	 */
	CLUBS,

	/**
	 * The suit diamonds
	 */
	DIAMONDS,

	/**
	 * The suit hearts
	 */
	HEARTS,

	/**
	 * The suit spades
	 */
	SPADES
}
